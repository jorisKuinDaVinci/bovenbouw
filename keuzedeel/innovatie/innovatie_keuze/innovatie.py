# Bereken Fibonacci-reeks tot n
def fibonacci(n):
    #GitHub Copilot vult automatisch de rest van de functie aan, bijvoorbeeld:
    a, b = 0, 1
    result = []
    while len(result) < n:
        result.append(a)
        a, b = b, a + b
    return result

